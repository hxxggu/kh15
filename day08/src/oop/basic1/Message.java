package oop.basic1;

//자바에게 메시지(Message)의 구조를 알려주기 위해 생성한 도구
//사용자가 만들었다고 해서 사용자 정의 자료형이라고 부른다
public class Message {
	// 구조를 설명
	String sender; //메시지에는 sender라는 문자열 구성 요소가 있어야 해요
	String content; //메시지에는 content라는 문자열 구성 요소가 있어야 해요
	String time; //메시지에는 time이라는 문자열 구성 요소가 있어야 해요
	int remain; //메시지에는 remain이라는 문자열 구성 요소가 있어야 해요
}
