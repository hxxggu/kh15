package loop;

public class Test01반복문 {
	public static void main(String[] args) {
		//반복
		//: 동일하거나 규칙이 있는 비슷한 내용들을 계속 실행하는 것
		//for, while, do-while 구문이 존재
		
		//Ex. hello 를 화면에 10번 출력하시오
		for(int i=1 ; i<=3 ; i++) { //10번
			System.out.println("hello");
			System.out.println("i = "+i);
		}
	}
}
